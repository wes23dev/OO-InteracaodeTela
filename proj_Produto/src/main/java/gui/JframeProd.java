    public void limparCampos(){
        txtNome.setText("");
        txtId.setText("");
        txtPreco.setText("");
        txtCusto.setText("");
    }
    
    public void habilitarCampos(boolean flag){
        txtNome.setEnabled(flag);
        txtId.setEnabled(flag);
        txtPreco.setEnabled(flag);
        txtCusto.setEnabled(flag);
    }
    
    public void objetoParaCampos(Produto p){
        txtNome.setText(p.getNomeProd());
        txtId.setText(p.getId());
        txtPreco.setText(p.getPreco()+ "");
        txtCusto.setText(p.getCusto()+ "");
        
    }
    
    public Produto camposParaObjeto(){
        Produto p = new Produto();
        
        p.setNomeProd(txtNome.getText());
        p.setId(txtId.getText());
        
        String precoTexto = txtPreco.getText();
        Double n = 0.0;
        if(!precoTexto.isEmpty()){
            n = Double.parseDouble(precoTexto);    
        }
        p.setPreco(n);
        
        String custoTexto = txtCusto.getText();
        Double c =0.0;
        if(!custoTexto.isEmpty()){
            c = Double.parseDouble(custoTexto);   
        }
        p.setCusto(c);
        
        return p;
        
    }


    
    

